#include "libft.h"

#include <stdio.h>

int		ft_isblank(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if (str[i] != ' ' && str[i] != '\t')
			return (0);
		++i;
	}
	return (-1);
}
