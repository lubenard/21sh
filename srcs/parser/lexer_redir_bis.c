#include <input.h>

void	which_redir(t_coord *c)
{
	if (c->c == '&')
	{
		c->k = '<';
		c->r = '>';
	}
	else
	{
		c->k = '&';
		c->r = c->c == '<' ? '>' : '<';
	}
}
